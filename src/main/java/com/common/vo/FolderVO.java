package com.common.vo;

public class FolderVO extends commonVO {

	
	
	private String folderIdx;
	private String folderSeq;
	private String folderName;
	private String id;
	private String upperFolder;
	private String folderDepth;
	private String del_flag;
	private String ins_dt;
	private String upt_dt;
	
	public String getFolderIdx() {
		return folderIdx;
	}
	public void setFolderIdx(String folderIdx) {
		this.folderIdx = folderIdx;
	}
	public String getFolderSeq() {
		return folderSeq;
	}
	public void setFolderSeq(String folderSeq) {
		this.folderSeq = folderSeq;
	}
	public String getFolderName() {
		return folderName;
	}
	public void setFolderName(String folderName) {
		this.folderName = folderName;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUpperFolder() {
		return upperFolder;
	}
	public void setUpperFolder(String upperFolder) {
		this.upperFolder = upperFolder;
	}
	public String getFolderDepth() {
		return folderDepth;
	}
	public void setFolderDepth(String folderDepth) {
		this.folderDepth = folderDepth;
	}
	public String getDel_flag() {
		return del_flag;
	}
	public void setDel_flag(String del_flag) {
		this.del_flag = del_flag;
	}
	public String getIns_dt() {
		return ins_dt;
	}
	public void setIns_dt(String ins_dt) {
		this.ins_dt = ins_dt;
	}
	public String getUpt_dt() {
		return upt_dt;
	}
	public void setUpt_dt(String upt_dt) {
		this.upt_dt = upt_dt;
	}
	
	
}
